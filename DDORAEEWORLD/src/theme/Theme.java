package theme;

public class Theme {
	private int theme_seq;
	private String theme_content;
	
	public int getTheme_seq() {
		return theme_seq;
	}
	public void setTheme_seq(int theme_seq) {
		this.theme_seq = theme_seq;
	}
	public String getTheme_content() {
		return theme_content;
	}
	public void setTheme_content(String theme_content) {
		this.theme_content = theme_content;
	}
}
